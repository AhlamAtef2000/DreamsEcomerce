<script>
    document.querySelectorAll('.variant-form').forEach(function(form) {
        form.querySelectorAll('.select-option').forEach(function(option) {
            option.addEventListener('click', function () {
                let type = option.getAttribute('data-type');
                let id = option.getAttribute('data-id');

                // Remove active from all of same type
                form.querySelectorAll(`.select-option[data-type="${type}"]`).forEach(el => el.classList.remove('active'));

                // Activate clicked one
                option.classList.add('active');

                // Update hidden input
                form.querySelector(`.selected-${type}`).value = id;
            });
        });
    });
</script>
