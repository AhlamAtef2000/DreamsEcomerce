<script>
    document.addEventListener("DOMContentLoaded", function () {
        const quickViewButtons = document.querySelectorAll('.quickview');

        quickViewButtons.forEach(btn => {
            btn.addEventListener('click', function (event) {
                event.preventDefault(); // Prevent default action

                const name = this.getAttribute('data-name');
                const price = this.getAttribute('data-price');
                const description = this.getAttribute('data-description');
                const image = this.getAttribute('data-image');
                const rating = parseInt(this.getAttribute('data-rating'));

                // Check if all data is present before opening the modal
                if (!name || !price || !description || !image) {
                    console.warn('Missing product data, modal will not open.');
                    return; // Don't open the modal
                }

                // Fill the modal with the product data
                document.getElementById('modalProductName').innerText = name;
                document.getElementById('modalProductPrice').innerText = `${price} JOD`;
                document.getElementById('modalProductDescription').innerText = description;

                const imageUrl = image ? `{{ asset('storage') }}${image}` : ''; // Construct the correct image URL
                document.getElementById('modalProductImage').src = imageUrl; // Set the image source

                // Add star ratings
                const ratingContainer = document.getElementById('modalProductRating');
                ratingContainer.innerHTML = ''; // Clear previous stars
                for (let i = 1; i <= 5; i++) {
                    const star = document.createElement('i');
                    star.className = i <= rating ? 'fas fa-star text-warning' : 'far fa-star text-muted';
                    ratingContainer.appendChild(star);
                }

                // Open the modal manually
                const modal = new bootstrap.Modal(document.getElementById('quickviewModal'));
                modal.show();

                // Listen for when the modal is completely hidden
                document.getElementById('quickviewModal').addEventListener('hidden.bs.modal', function () {
                    // Reset modal content when it's closed
                    document.getElementById('modalProductImage').src = ''; // Reset image
                    document.getElementById('modalProductName').innerText = ''; // Reset name
                    document.getElementById('modalProductPrice').innerText = ''; // Reset price
                    document.getElementById('modalProductDescription').innerText = ''; // Reset description
                    document.getElementById('modalProductRating').innerHTML = ''; // Reset rating

                    // Manually remove the backdrop (this might not be necessary with the latest Bootstrap version)
                    const backdrop = document.querySelector('.modal-backdrop');
                    if (backdrop) {
                        backdrop.classList.remove('show');
                        setTimeout(() => {
                            backdrop.remove(); // Ensure the backdrop is removed from the DOM
                        }, 150); // Slight delay to let the transition finish
                    }
                });
            });
        });
    });
</script>





